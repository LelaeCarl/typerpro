export const generateWords = (count: number = 200): string[] => {
  const words: string[] = [];
  
  for (let i = 0; i < count; i++) {
    const randomIndex = Math.floor(Math.random() * DEFAULT_WORDS.length);
    words.push(DEFAULT_WORDS[randomIndex]);
  }
  
  return words;
};

export const generateWordTokens = (count: number = 200) => {
  const words = generateWords(count);
  return words.map(word => ({
    target: word,
    letters: new Array(word.length).fill('pending'),
    typed: ''
  }));
};
